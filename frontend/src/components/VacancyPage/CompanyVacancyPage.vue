<template>
  <v-app>
    <v-card>
      {{vacancy.name}}
      {{vacancy.description}}
      {{vacancy.money}}
      {{vacancy.ownerName}}
      <ul>
        <li v-for="skill in vacancy.requestedSkills">
          {{ skill }}
        </li>
      </ul>
    </v-card>
    <v-card v-if="task.name">
      {{task.name}}
      {{task.description}}
    </v-card>


        <v-data-table
                :headers="headers"
                :items="responses"
                class="elevation-1"
                sort-by="calories"
        >
            <template v-slot:top>
                <v-toolbar color="white" flat>
                    <v-toolbar-title>Responces</v-toolbar-title>
                </v-toolbar>
            </template>
            <template v-slot:item.action="{ item }">
        <v-icon
            @click="downloadSubmission(item)"
        >
          mdi-cloud-download-outline
        </v-icon>


                <v-icon @click="accept(item)"
                        v-if="item.status === 'APPLIED'"
                >
                    mdi-check-outline
                </v-icon>

                <v-icon
                        @click="reject(item)"
                        v-if="item.status === 'APPLIED'"
                >
                    mdi-close-outline
                </v-icon>
                <v-icon
                        @click="route(item)"
                >
                    mdi-account-badge-horizontal-outline
                </v-icon>
            </template>
            <template v-slot:no-data>
                You have no active responses
            </template>
        </v-data-table>
    </v-app>
</template>

<script>

  import Router from "../../router"
  import {urlPort} from "../../tool";
  import download from "downloadjs"


  export default {
    name: "companyVacancyPage",
    data: function () {
      return {
        fileBytes: null,
        vacancy: {
          id: Router.currentRoute.params.id,
          name: "",
          description: "",
          money: null,
          ownerName: "",
          requestedSkills: [],
          withTask: null,
        },
        task: {
          id: null,
          name: "",
          description: "",

        },
        responses: [],
        submissions: [],
        headers: [
          {text: 'Applicant name', value: 'applicantName',},
          {text: 'Status', value: 'status',},
          {text: 'Actions', value: 'action', sortable: false},
        ],
      }
    },
    created() {
      urlPort.get("/vacancies/" + this.vacancy.id)
        .then(resp => {
          this.vacancy = resp.data;
          urlPort.get("/my/vacancies/" + this.vacancy.id)
            .then(resp => {
              this.responses = resp.data
            });
          if (this.vacancy.withTask) {
            urlPort.get("/vacancies/" + this.vacancy.id + "/tasks")
              .then(resp => {
                this.task = resp.data;
                urlPort.get("/vacancies/" + this.vacancy.id + "/tasks/" + this.task.id)
                  .then(resp => {
                    this.submissions = resp.data;
                  })
              });

          }
        });
    },
        methods: {
            accept(item) {
                item.status = "INVITED";
                this.setStatus(item);
            },
            reject(item) {
                item.status = "REJECTED";
                this.setStatus(item);
            },
            route(item) {
                //todo: сделать линкк на резюме пользователя
            },
            setStatus(item) {
                let params = new URLSearchParams();
                params.append("status", item.status);
                urlPort.post("/responses/" + item.id, params)
            },
            downloadSubmission(item) {
                this.fileBytes = this.submissions.find(function (element, index, array) {
                    return element.applicantId === item.applicantId
                }).fileData;
                download(atob(this.fileBytes), item.applicantName + "submission.zip", "application/zip")
            }
    },
  }
</script>

